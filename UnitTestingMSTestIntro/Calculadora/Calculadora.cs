using System;
namespace Calculadora
{
    public class Calcs
    {
        public static int Sum(int a, int b)
        {
            return a + b;
        }
        public static int Resta(int a, int b)
        {
            return a - b;
        }
        public static int Multiplicacion(int a, int b)
        {
            return a * b;
        }
        public static int Division(int a, int b)
        {
            return a / b;
        }
    }
}
